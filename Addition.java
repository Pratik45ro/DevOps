public class Addition {
    public static void main(String[] args) {
        // Hardcoded values for the numbers
        int num1 = 10;  // Example value for the first number
        int num2 = 20;  // Example value for the second number

        // Adding the numbers
        int sum = num1 + num2;

        // Output the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
